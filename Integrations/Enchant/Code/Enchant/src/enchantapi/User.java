package enchantapi;

public class User {
	public String id;
	public String first_name;
	public String last_name;
	public String email;
	
	public User(String id,String firstname,String lastname,String email){
		this.id=id;
		this.first_name=firstname;
		this.last_name=lastname;
		this.email=email;
	}
	

}
